package by.it.arhipova.jd01_10;

public class Bean {
    double sum(int a, int b){
        return a+b;

    }
    static double min(int a, int b){
        return a<b?a:b;

    }
    double max(int a, int b){
        return a>b?a:b;

    }
    static double avg(int a, int b){
        return (a+b) / 2.0;
    }
}
